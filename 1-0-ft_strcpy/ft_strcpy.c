char		*ft_strcpy(char *s1, char *s2)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = s1;
	while (s2[i] != '\0')
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	s1 = ptr;
	return (s1);
}
